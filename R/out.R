#------------------------------------------------------------------------------#
#' Value matching (extra)
#'
#' @inheritParams base::`%in%`
#'
#' @name out
#' @export
#------------------------------------------------------------------------------#
`%out%` <- function(x, table) match(x, table, nomatch = 0L) == 0L

#------------------------------------------------------------------------------#
#' @rdname out
#' @export
#------------------------------------------------------------------------------#
`%!in%` <- `%out%`
